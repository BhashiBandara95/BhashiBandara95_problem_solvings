public class Main{
    public static void main(String[] args){
       // declare the initial variable
        int x = 1;
        // loop the numbers 1 to 10
        while(x <= 10){
            // check the even numbers
            if(x % 2 == 0){
                System.out.println(x);
            }
            // increment the number
            x ++;
        }
    }
}